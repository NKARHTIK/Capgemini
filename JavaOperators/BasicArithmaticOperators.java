package JavaOperators;

public class BasicArithmaticOperators {

	public static void main(String[] args) {
		//Arithmetic operators 
		//1. Addition 2. Subtraction 3.Multiplication 4.Division 5. Modulus
		int number_one=100;
		int number_two=200;
		//Addition
		System.out.println("Addition of two numbers : " + (number_one+number_two));
		//subtract
		System.out.println("Subtraction of two numbers: " + (number_one-number_two));
		//Multiple
		System.out.println("Multiple of two numbers : " + (number_one*number_two));
		//Division
		System.out.println("Division of two number : " + (number_one/number_two));
		//Modulus
		System.out.println("Modulus of two number : " + (number_one%number_two));
	}
}
